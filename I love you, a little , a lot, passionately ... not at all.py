def how_much_i_love_you(nb_petals):
    if nb_petals == 7 :
        return "I love you"
    elif nb_petals == 3:
        return "a lot"
    elif nb_petals == 2:
        return "a little"
    elif nb_petals == 4:
        return "a passionately"
    elif nb_petals == 5:
        return "madly"
    elif nb_petals == 6 :
        return "not at all"
